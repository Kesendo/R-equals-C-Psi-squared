MATCH = 1e-8

X1 = np.array([[0, 1], [1, 0]], dtype=complex)
Y1 = np.array([[0, -1j], [1j, 0]], dtype=complex)
Z1 = np.array([[1, 0], [0, -1]], dtype=complex)
# the INVOLUTIVE letter-transposition Cliffords (reflections, order 2 as Ad):
T_YZ = (Y1 + Z1) / np.sqrt(2.0)     # Y<->Z, X->-X
T_XY = (X1 + Y1) / np.sqrt(2.0)     # X<->Y, Z->-Z
# (T_XZ = Hadamard (HAD): X<->Z, Y->-Y -- already an involution)


def pi_coh(N, letter):
    """Pi_letter on coherence space, conj-aligned to the typed convention Pi_Z = R.D.

    build_pi_full uses the opposite i-phase (Y->+iZ), so its Pi is Pi^-1 relative to R.D;
    complex-conjugating the signed permutation inverts the order-4 element back to Pi_Z.
    (Diagnosed via the Stage-0 gate: dev 1.0 -> 0. D.Pi_Z.D = Pi_Y holds in both conventions.)"""
    M = _vec_to_pauli_basis_transform(N)
    Minv = M.conj().T / (2 ** N)
    return M @ build_pi_full(N, letter).conj() @ Minv


def ops(N):
    """R, D (the D4 core) and the involutive letter-transposition superoperators on coherence space."""
    d = 2 ** N
    Id = np.eye(d, dtype=complex)
    R = np.kron(Id, xn(N))            # R: rho -> rho F,  F = X^(x)N  (ONE-SIDED)
    D = swap_perm(N)                  # D: rho -> rho^T
    h_zx = ad_unitary(N, HAD)         # X<->Z involution
    t_yz = ad_unitary(N, T_YZ)        # Y<->Z involution
    t_xy = ad_unitary(N, T_XY)        # X<->Y involution
    return R, D, h_zx, t_yz, t_xy


def order_of(g, maxo=64):
    P = np.eye(g.shape[0], dtype=complex)
    for k in range(1, maxo + 1):
        P = P @ g
        if np.max(np.abs(P - np.eye(g.shape[0]))) < MATCH:
            return k
    return None


# ============================ Stage 0: conventions + D4 core (the gate) ============================
def stage0_gate(N=2):
    print(f"Stage 0 (gate): Pi_Z=R.D, D.Pi_Z.D=Pi_Y, |<R,D>|=8 on coherence space (N={N}):")
    R, D, *_ = ops(N)
    PiZ, PiY = pi_coh(N, "Z"), pi_coh(N, "Y")
