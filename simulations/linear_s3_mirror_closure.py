    dev_inv = np.max(np.abs(D @ PiZ @ D - PiY))
    d4 = group_closure([R, D], (2 ** N) ** 2)
    print(f"   |Pi_Z - R.D| = {dev_fac:.2e}   |D.Pi_Z.D - Pi_Y| = {dev_inv:.2e}   |<R,D>| = {len(d4)}")
